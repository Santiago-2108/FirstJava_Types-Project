package com.company;

public class JavaDataTypes {
    public static void main(String[] args) {

        byte ages = -128;
        System.out.println("byte variable type = " + ages);

        int age = 12;
        System.out.println("int variable type = " +age);

        boolean answer = true;
        System.out.println("Boolean variable type = " +answer);

        short money =  32_766;
        System.out.println("Short variable type = " + money);

        long VillagePopulation = 2147483646l;
        System.out.println("Long variable type = " + VillagePopulation);

        float area = 12345678.90f;
        System.out.println("Float variable type = " + area);



    }
}
